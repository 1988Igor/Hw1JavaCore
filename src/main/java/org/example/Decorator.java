package org.example;

public class Decorator {

    /*
    The helloUser method takes two parameters: a String named name and an int named age.
    It simply prints a greeting message to the console, including the provided name and age
     */
    public static void  helloUser(String name, int age) {
        System.out.println("Hello " + name + "! Your age is: " + age);
    }
}
